    def initialize(at_mass, number, period, group, type, quantum_numbers, isotops)
      @at_mass = at_mass
      @number = number
      @period = period
      @group = group
      @type = type
      @quantum_numbers = quantum_numbers #type check!
      @isotops = isotops #type_check!
    end

  end

  class Substance
    def initialize(elements)
      @elements = elements #type check! for example: {{H: 2}, {S: 1}, {O: 4}}
    end
